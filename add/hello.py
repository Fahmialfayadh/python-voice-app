def print_hello():
    
    hello_pattern = [
        " **       **   ******   **       **        ***********",
        " **       **   **       **       **        **       **",
        " ** * * * **   ******   **       **        **       **",
        " **       **   **       **       **        **       **",
        " **       **   ******   ******   ******    *********** "
    ]
    
    # Print each line in the pattern
    for line in hello_pattern:
        print(line)

# Call the function to print the pattern
print_hello()